package Bean;

public class ShopBean {
	private int id;
	private String unique_id;
	private String item_type;
	private String item_name;
	private int item_price;
	private int item_count;
	private String item_cat;
	private String item_dec;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getUnique_id() {
		return unique_id;
	}
	public void setUnique_id(String unique_id) {
		this.unique_id = unique_id;
	}
	public String getItem_type() {
		return item_type;
	}
	public void setItem_type(String item_type) {
		this.item_type = item_type;
	}
	public String getItem_name() {
		return item_name;
	}
	public void setItem_name(String item_name) {
		this.item_name = item_name;
	}
	public int getItem_price() {
		return item_price;
	}
	public void setItem_price(int item_price) {
		this.item_price = item_price;
	}
	public int getItem_count() {
		return item_count;
	}
	public void setItem_count(int item_count) {
		this.item_count = item_count;
	}
	public String getItem_cat() {
		return item_cat;
	}
	public void setItem_cat(String item_cat) {
		this.item_cat = item_cat;
	}
	public String getItem_dec() {
		return item_dec;
	}
	public void setItem_dec(String item_dec) {
		this.item_dec = item_dec;
	}
}
